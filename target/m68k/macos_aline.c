void build_alinetrap_hash(M68kCPU *cpu)
{
    CPUM68KState *env = &cpu->env;
    ALine aline, *pa;
    int i;

    env->alinetraps = g_hash_table_new(g_direct_hash, g_direct_equal);

    for (i = 0; i < (sizeof(atraps) / sizeof(atraps[0])); i++) {
        aline = atraps[i];
        pa = malloc(sizeof(ALine));
        memcpy(pa, &aline, sizeof(ALine));

        g_hash_table_insert(env->alinetraps, GINT_TO_POINTER(aline.opcode), (gpointer)pa);
    }
}

const ALine *lookup_alinetrap(GHashTable *g, gint opcode)
{
    return g_hash_table_lookup(g, GINT_TO_POINTER(opcode));
}
